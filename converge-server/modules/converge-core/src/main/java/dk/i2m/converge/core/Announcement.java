package dk.i2m.converge.core;

import java.io.Serializable;
import java.util.Calendar;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Lob;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.persistence.Temporal;

/**
 * System {@link Announcement} for all users to see.
 *
 * @author Allan Lykke Christensen
 */
@Entity
@Table(name = "announcement")
@NamedQueries({
    @NamedQuery(name = Announcement.FIND_PUBLISHED, query = "SELECT a FROM Announcement a WHERE a.published = true ORDER BY a.date DESC")
})
public class Announcement implements Serializable {

    /** Query for finding all published announcements sorted by date (descending). */
    public static final String FIND_PUBLISHED = "Announcement.findPublished";

    private static final long serialVersionUID = 1L;

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id")
    private Long id;

    @Temporal(javax.persistence.TemporalType.TIMESTAMP)
    @Column(name = "announcement_date")
    private Calendar date;

    @Column(name = "title")
    private String title;

    @Column(name = "description") @Lob
    private String description;

    @Column(name = "thumb")
    private String thumb;

    @Column(name = "published")
    private boolean published = false;

    /**
     * Creates a new instance of {@link Announcement}.
     */
    public Announcement() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Calendar getDate() {
        return date;
    }

    public void setDate(Calendar date) {
        this.date = date;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getThumb() {
        return thumb;
    }

    public void setThumb(String thumb) {
        this.thumb = thumb;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public boolean isPublished() {
        return published;
    }

    public void setPublished(boolean published) {
        this.published = published;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        if (!(object instanceof Announcement)) {
            return false;
        }
        Announcement other = (Announcement) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return getClass().getName() + "[id=" + id + "]";
    }
}
