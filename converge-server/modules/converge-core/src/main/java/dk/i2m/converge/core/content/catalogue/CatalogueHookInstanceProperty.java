package dk.i2m.converge.core.content.catalogue;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.Lob;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

/**
 *
 * @author Allan Lykke Christensen
 */
@Entity
@Table(name = "catalogue_hook_property")
public class CatalogueHookInstanceProperty implements Serializable {

    private static final long serialVersionUID = 1L;

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id")
    private Long id;

    @ManyToOne(fetch = FetchType.EAGER)
    @JoinColumn(name = "catalogue_hook_id")
    private CatalogueHookInstance catalogueHook;

    @Column(name = "property_key")
    private String key;

    @Column(name = "property_value") @Lob
    private String value;

    public CatalogueHookInstanceProperty() {
    }

    public CatalogueHookInstance getCatalogueHook() {
        return catalogueHook;
    }

    public void setCatalogueHook(CatalogueHookInstance catalogueHook) {
        this.catalogueHook = catalogueHook;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final CatalogueHookInstanceProperty other = (CatalogueHookInstanceProperty) obj;
        if (this.id != other.id && (this.id == null || !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 89 * hash + (this.id != null ? this.id.hashCode() : 0);
        return hash;
    }
}
